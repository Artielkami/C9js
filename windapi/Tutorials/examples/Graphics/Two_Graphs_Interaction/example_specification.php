In this example, we have two graph components.<br>
We add two annotation to the graph and two annotations to the text ("2014-06-11", "2014-06-12").<br>
We create a "click" event for each annotation.<br>
<ul>
<li>If we "click" on "<span style="color:red">2014-06-11</span>" either on the graph or on the text annotation, the reaction "highlight" will be activated on both annotations.</li>
<li>If we "click" on "<span style="color:green">2014-06-12</span>" either on the graph or on the text annotation, the reaction "highlight" will be activated on both annotations.</li>
</ul>