In this example, we add 3 annotations each on a different vector layer.<br>
The <span style="color:blue">blue annotation</span> on the vector layer 0.<br>
The <span style="color:red">red annotation</span> on the vector layer 1.<br>
The <span style="color:green">green annotation</span> on the vector layer 2.<br>
Here we choose that vector layers 0 and 1 are active at the same time.<br>
<ul>
<li>If we "click" on the<span style="color:red"> red annotation</span>, the <span style="color:green">green annotation</span> will disappear.</li>
<li>If we "click" on the<span style="color:blue"> blue annotation</span>, the <span style="color:green">green annotation</span> will reappear.</li>
</ul>