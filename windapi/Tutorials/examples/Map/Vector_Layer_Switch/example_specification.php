In this example, we add 3 annotations each on a different vector layer.<br>
The <span style="color:blue">blue annotation</span> on the vector layer 0.<br>
The <span style="color:red">red annotation</span> on the vector layer 1.<br>
The <span style="color:green">green annotation</span> on the vector layer 2.<br>
Here we switch between vector layers 0 and 1 using a button. Only one vector layer is active at every moment.<br>
<ul>
<li>If we choose vector layer 1 and "click" on<span style="color:red"> red annotation</span>, the <span style="color:green">green annotation</span> will disappear.</li>
<li>If we choose vector layer 0 and "click" on<span style="color:blue"> blue annotation</span>, the <span style="color:green">green annotation</span> will reappear.</li>
</ul>