In this example, we have a map component and a text component.<br>
We add two annotation to the map and two annotations to the text ("Bayonne", "Pau").<br>
We create a "click" event for each annotation.<br>
<ul>
<li>If we "click" on "<span style="color:red">Bayonne</span>" either on the map or on the text annotation, the reaction "highlight" will be activated on the annotation of the map and the text.</li>
<li>If we "click" on "<span style="color:green">Pau</span>" either on the map or on the text annotation, the reaction "highlight" will be activated on the annotation of the map and the text.</li>
</ul>