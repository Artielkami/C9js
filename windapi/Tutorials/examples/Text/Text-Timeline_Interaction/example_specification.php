In this example, we have a timeline component and a text component.<br>
We add one annotation to the timeline and one annotation to the text("2014-07-16 at 17:00").<br>
We create a "click" event for each annotation.<br>
<ul>
<li>If we "click" on the annotation either on the timeline or on the text annotation, the reaction "highlight" will be activated on the annotation of the timeline and the text.</li>
</ul>