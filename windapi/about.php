            <div class="pageContent">
				<p>WIND API <?php echo $wind_version; ?> is a JavaScript library for manipulating uniformly (independent of the used component) visual, text, cartographic and temporal components.</p>

				<p>WIND API allows to abstract mapping providers (<a href="http://openlayers.org/" class="lien" target="_blank">OpenLayers v2.13.1</a>, <a  class="lien" href="https://developers.google.com/maps/documentation/javascript/tutorial" target="_blank">Google Maps v3</a>, <a  class="lien" href="http://api.ign.fr" target="_blank">Geoportail IGN</a>, <a  class="lien" href="https://openstreetmap.fr/" target="_blank">OpenStreetMap</a>, <a  class="lien" href="https://developer.yahoo.com/maps/" target="_blank">Yahoo! Maps</a>, <a  class="lien" href="http://msdn.microsoft.com/en-us/library/dd877180.aspx" target="_blank">Bing Maps</a>).</p>
				
				<p>For time information, WIND API is based on <a  class="lien" href="http://www.simile-widgets.org/timeline/" target="_blank">Simile Timeline v2.3.0</a> and <a  class="lien" href="http://almende.github.io/chap-links-library/timeline.html" target="_blank">Chap Links Timeline.</a></p>
